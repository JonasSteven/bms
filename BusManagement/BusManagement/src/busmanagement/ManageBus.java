    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        try{
            Class.forName("com.mysql.cj.jdbc.Driver");

            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/bms", "root", "");
            String sql = "insert into managebus values (?,?,?,?,?)";
            PreparedStatement ptst = con.prepareStatement(sql);
            ptst.setString(1, bc.getText());
            ptst.setString(2, s.getText());
            ptst.setString(3, d.getText());
            ptst.setString(4, dpt.getText());
            ptst.setString(5, nos.getText());

            if(bc.getText().startsWith("BU") && (s.getText().length() >= 5 && s.getText().length() <= 20) && (d.getText().length() >= 5 && d.getText().length() <= 20) && (dpt.getText().matches("[0-9][0-9][0-9][0-9]-[0-9][0-9]-[0-9][0-9] [0-9][0-9]:[0-9][0-9]:[0-9][0-9]")) && (nos.getText().matches("[0-9]*")) && (nos.getText().length() >= 1 && nos.getText().length() <= 2)){
                ptst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data inserted Succesfully!");
                bc.setText("");
                s.setText("");
                d.setText("");
                dpt.setText("");
                nos.setText("");
                con.close();
            }else if(bc.getText().isEmpty() && s.getText().isEmpty() && d.getText().isEmpty() && dpt.getText().isEmpty() && nos.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Data cannot be empty!");
            }else if(!bc.getText().startsWith("BU")){
                JOptionPane.showMessageDialog(this, "Bus Code must starts with 'BU'");
            }else if(s.getText().length() < 5 || s.getText().length() > 20){
                JOptionPane.showMessageDialog(this, "Source's length must be 5 - 20 digits");
            }else if(d.getText().length() < 5 || d.getText().length() > 20){
                JOptionPane.showMessageDialog(this, "Destination's length must be 5 - 20 digits");
            }else if(!dpt.getText().matches("[0-9][0-9][0-9][0-9]-[0-9][0-9]-[0-9][0-9] [0-9][0-9]:[0-9][0-9]:[0-9][0-9]")){
                JOptionPane.showMessageDialog(this, "Depart format input must be 'yyyy-mm-dd HH:MM:SS'");
            }else if(!nos.getText().matches("[0-9]*") ||nos.getText().length() < 1 || nos.getText().length() > 2){
                JOptionPane.showMessageDialog(this, "No of Seats's must be only number and the length must be 1 - 2 digits");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

