    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:

        try{
            Class.forName("com.mysql.cj.jdbc.Driver");

            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/bms", "root", "");
            Statement st = con.createStatement();

            String id = bc.getText();
            String sql2 = "Select buscode from managebus";
            ResultSet rs = st.executeQuery(sql2);
            boolean flag = false;

            while(rs.next()){
                String busCode = rs.getString("buscode");

                if(id.equals(busCode)){
                    flag = true;
                }
            }

            if(flag && id.startsWith("BU")){
                String sql = "DELETE FROM `managebus` WHERE buscode ='"+id+"'";
                PreparedStatement ptst = con.prepareStatement(sql);
                ptst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data deleted Succesfully!");
                bc.setText("");
                con.close();
            }else if(id.isEmpty()){
                JOptionPane.showMessageDialog(this, "Please, Input Bus Code to Delete Data!");
            }else if(!id.startsWith("BU")){
                JOptionPane.showMessageDialog(this, "Bus Code must starts with 'BU'");
            }else if(id.startsWith("BU") && !flag){
                JOptionPane.showMessageDialog(this, "Data not exist");
                bc.setText("");
            }

        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

