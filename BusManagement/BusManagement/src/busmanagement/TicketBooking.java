    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        try{
            Class.forName("com.mysql.cj.jdbc.Driver");

            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/bms", "root", "");
            String sql = "insert into ticketbooking values (?,?,?,?,?,?,?)";
            PreparedStatement ptst = con.prepareStatement(sql);
            ptst.setString(1, pid.getText());
            ptst.setString(2, pn.getText());
            ptst.setString(3, bc.getText());
            ptst.setString(4, g.getText());
            ptst.setString(5, ppn.getText());
            ptst.setString(6, amt.getText());
            ptst.setString(7, n.getText());

            if(pid.getText().startsWith("PA") && (pn.getText().length() >= 5 && pn.getText().length() <= 30) && bc.getText().startsWith("BU") && (g.getText().equals("Male") || g.getText().equals("Female")) && ppn.getText().startsWith("PPN") && (amt.getText().matches("[0-9]*") && !amt.getText().isEmpty()) && (n.getText().equals("Indonesia") || n.getText().equals("Malaysia") || n.getText().equals("Singapore"))){
                ptst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data Booked Succesfully!");
                con.close();
            }else if(pid.getText().isEmpty() && pn.getText().isEmpty() && bc.getText().isEmpty() && g.getText().isEmpty() && ppn.getText().isEmpty() && amt.getText().isEmpty() && n.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Data cannot be empty!");
            }else if(!pid.getText().startsWith("PA")){
                JOptionPane.showMessageDialog(this, "Passenger ID must be starts with 'PA'");
            }else if(pn.getText().length() < 5 || pn.getText().length() > 30){
                JOptionPane.showMessageDialog(this, "Passenger Name's length must be 5 - 30 digits");
            }else if(!bc.getText().startsWith("BU")){
                JOptionPane.showMessageDialog(this, "Bus Code must be starts with 'BU'");
            }else if(!g.getText().equals("Male") && !g.getText().equals("Female")){
                JOptionPane.showMessageDialog(this, "Gender must be 'Male' or 'Female'");
            }else if(!ppn.getText().startsWith("PPN")){
                JOptionPane.showMessageDialog(this, "Passport Number must be starts with 'PPN'");
            }else if(!amt.getText().matches("[0-9]*") || amt.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Amount must be only number");
            }else if(!n.getText().equals("Indonesia") && !n.getText().equals("Malaysia") && !n.getText().equals("Singapore")){
                JOptionPane.showMessageDialog(this, "Nationality must be 'Indonesia', 'Malaysia' or 'Singapore'");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

